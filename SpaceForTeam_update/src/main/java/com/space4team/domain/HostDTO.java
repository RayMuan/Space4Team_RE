package com.space4team.domain;

public class HostDTO {
	private String h_no;
	private String h_id;
	private String h_pass;
	private String h_name;
	private String h_email;
	
	//get set
	public String getH_no() {
		return h_no;
	}
	public void setH_no(String h_no) {
		this.h_no = h_no;
	}
	public String getH_id() {
		return h_id;
	}
	public void setH_id(String h_id) {
		this.h_id = h_id;
	}
	public String getH_pass() {
		return h_pass;
	}
	public void setH_pass(String h_pass) {
		this.h_pass = h_pass;
	}
	public String getH_name() {
		return h_name;
	}
	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	public String getH_email() {
		return h_email;
	}
	public void setH_email(String h_email) {
		this.h_email = h_email;
	}
}
