package com.space4team.domain;

public class GuestDTO {
	private String g_no;
	private String g_id;
	private String g_pass;
	private String g_name;
	private String g_email;
	private String g_phone;
	
	//get set
	public String getG_no() {
		return g_no;
	}
	public void setG_no(String g_no) {
		this.g_no = g_no;
	}
	public String getG_id() {
		return g_id;
	}
	public void setG_id(String g_id) {
		this.g_id = g_id;
	}
	public String getG_pass() {
		return g_pass;
	}
	public void setG_pass(String g_pass) {
		this.g_pass = g_pass;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_email() {
		return g_email;
	}
	public void setG_email(String g_email) {
		this.g_email = g_email;
	}
	public String getG_phone() {
		return g_phone;
	}
	public void setG_phone(String g_phone) {
		this.g_phone = g_phone;
	}
}
